package com.busanit501.api5012.domain.library;

/**
 * MemberRole - 도서관 회원 역할(권한) 열거형
 *
 * 회원이 가질 수 있는 역할을 정의합니다.
 * Spring Security의 권한 체계와 연동되어 API 접근 제어에 사용됩니다.
 *
 * 사용 예:
 *   - USER  : 일반 회원 (도서 대여, 이벤트 신청 등)
 *   - ADMIN : 관리자 (도서 등록/수정/삭제, 공지 관리 등)
 */
public enum MemberRole {

    /**
     * USER - 일반 회원
     * 도서 대여, 반납, 이벤트 신청, 시설 예약, 희망 도서 신청 등의
     * 기본 서비스를 이용할 수 있는 일반 사용자 역할입니다.
     */
    USER,

    /**
     * ADMIN - 관리자
     * 도서 등록/수정/삭제, 공지사항 관리, 이벤트 관리,
     * 대여 현황 관리 등 모든 관리 기능을 사용할 수 있는 역할입니다.
     */
    ADMIN
}
